/* AArch64 Kernel Entry Point and Exception Vectors */

.section .text

.global kentry
kentry:
    /* x0 contains the BootInfo pointer from Limine */
    /* Set up a stack if needed, but Limine provides one */
    b _start

.balign 2048
.global exception_vector_table
exception_vector_table:
    /* Current EL with SP0 */
    .balign 128
    b .
    .balign 128
    b .
    .balign 128
    b .
    .balign 128
    b .

    /* Current EL with SPx */
    .balign 128
    b handle_sync_exception
    .balign 128
    b handle_irq
    .balign 128
    b .
    .balign 128
    b .

    /* Lower EL using AArch64 */
    .balign 128
    b handle_sync_exception
    .balign 128
    b handle_irq
    .balign 128
    b .
    .balign 128
    b .

    /* Lower EL using AArch32 */
    .balign 128
    b .
    .balign 128
    b .
    .balign 128
    b .
    .balign 128
    b .

.macro SAVE_CONTEXT
    sub sp, sp, #272 // Size of Context struct
    stp x0, x1, [sp, #0]
    stp x2, x3, [sp, #16]
    stp x4, x5, [sp, #32]
    stp x6, x7, [sp, #48]
    stp x8, x9, [sp, #64]
    stp x10, x11, [sp, #80]
    stp x12, x13, [sp, #96]
    stp x14, x15, [sp, #112]
    stp x16, x17, [sp, #128]
    stp x18, x19, [sp, #144]
    stp x20, x21, [sp, #160]
    stp x22, x23, [sp, #176]
    stp x24, x25, [sp, #192]
    stp x26, x27, [sp, #208]
    stp x28, x29, [sp, #224]
    str x30, [sp, #240]
    
    mrs x0, elr_el1
    mrs x1, spsr_el1
    stp x0, x1, [sp, #248]
.endm

.macro RESTORE_CONTEXT
    ldp x0, x1, [sp, #248]
    msr elr_el1, x0
    msr spsr_el1, x1

    ldp x0, x1, [sp, #0]
    ldp x2, x3, [sp, #16]
    ldp x4, x5, [sp, #32]
    ldp x6, x7, [sp, #48]
    ldp x8, x9, [sp, #64]
    ldp x10, x11, [sp, #80]
    ldp x12, x13, [sp, #96]
    ldp x14, x15, [sp, #112]
    ldp x16, x17, [sp, #128]
    ldp x18, x19, [sp, #144]
    ldp x20, x21, [sp, #160]
    ldp x22, x23, [sp, #176]
    ldp x24, x25, [sp, #192]
    ldp x26, x27, [sp, #208]
    ldp x28, x29, [sp, #224]
    ldr x30, [sp, #240]
    add sp, sp, #272
    eret
.endm

handle_sync_exception:
    SAVE_CONTEXT
    mrs x0, esr_el1
    mrs x1, far_el1
    bl handle_exception_zig
    RESTORE_CONTEXT

handle_irq:
    SAVE_CONTEXT
    bl handle_irq_zig
    RESTORE_CONTEXT
